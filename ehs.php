                                <h5>EHS DESCRIPTION</h5>

    <h6>We are an emerging Community Global Health Organization
        with a mission of providing Universal Primary Health Care System for Sub-Sahara African citizen's.</h6>

                                <h6>Our programme (EHS) aligns with the Institute of Health Care Improvement 2007 Triple Aim conceptual framework, which seeks
                                    improved patient health care experience, improved health for rural populace, and reduced per capita health care cost among Sub Saharan Regions.</h6>
                                <h6>Through this initiative, EHS was founded on
                                    the need to serve the vulnerable populations, make health care coverage a universal right, and improve the quality of life.  </h6>
                                <h6>The founder of this organization envisions' a global health system in Sub-Saharan Africa where health care is affordable by all. It is known globally, that Countries in Sub-Sahara Africa have high rate of infant and maternal mortality rate according to statistics.</h6>
                                <h6> A sizable number of these Sub Saharan African countries are low GDP income countries with their standard of living pegged at under $2.00 a day.
                                     A host of whom die as a result of lack of health care infrastructure, limited number of health care workers.</h6>
                                <h6>As a solution to mitigate these avoidable deaths, EHS is guided within
                                    the framework to create a health care system that is universal, accessible, and portable to the need of the population we serve.</h6>



                                <h4>Processes of EHS Operations...</h4>

<h6><span class="text-primary">Outcome 1:</span> minimize the risk of infant mortality. We follow every newborn baby for up to 1 year to make sure they receive all vaccinations per WHO/EHS and country guideline. </h6>
<h6><span class="text-primary">Outcome 2:</span>reduce maternal mortality rate. We deliver 24 hours emergency services. We engage pregnant women from the first trimester to the third trimester until the birth of the child. We make sure they receive antenatal care services per WHO/country specific guideline.</h6>
<h6><span class="text-primary">Outcome 3:</span> economic empowerment. A healthy nation is an independent nation. We empower the population we serve through equitable access to healthcare. We strive to minimize potential years of life lost and increase economic productivity through a healthy life. </h6>
<h6><span class="text-primary">Outcome 4:</span> increase lifespan. Our model of care dictates the reduction of disability life adjusted years due to the burden of disease within the population we serve. We are remotely friendly. We work in hard to reach area to deliver life-saving drugs. Our 24 hours emergency ambulance services are cost-free. </h6>

                                <h4>EHS Fundamental Principle in Patient care</h4>

                                <h6><span class="text-primary">Patient-centered:</span> it is all about our patients. We do not sleep if our patients are awaked. We provide care with a charitable heart. Our patients, family, and loved ones are the reason we are here. We take a holistic approach to care for those who need us the most in difficult times.</h6>
                                <h6><span class="text-primary">Population health-centered:</span> we are driven by a holistic and integrated health model. EHS takes into account social, biological, environmental, economic, cultural, and financial determinants of health. These drivers that change the course of health are addressed in our model of universal access to care to reduce possible risk to patients.</h6>
<h6>	<span class="text-primary">Safety:</span> we provide culture of safety. Our patients are in a safe environment and in the hands of caring staffs. We strive to provide zero risks to harm, injury or hurt. EHS is a safe place.</h6>
<h6><span class="text-primary">Patients Experience and satisfaction:</span> EHS top priority is patients’ satisfaction. We walk our patients through every step of their care. We make sure patients have the right time and right pre-post treatments. We evaluate patients experience through surveys.</h6>
               <h6><span class="text-primary">Stewardship:</span> we are a universal health system. EHS does not reject patients based on financial hardship. We partner with local hospitals and international organizations to bring the latest clinical care to underserved populations.  We go where we are called. We provide automatic free services for disabling and elderly people 65 years and above. We understand that most people consume healthcare when they are in their late age of life. In the developing world affordability is a problem. That is one of the reasons our model of care is absolutely seniors and disable friendly.</h6>
                            <h6><span class="text-primary">Transparency:</span> we are honest and open-minded in quality care  </h6>


                                <h4>OUR VALUES</h4>
                                <h6><span class="text-primary">Integrity:</span> we emphasize honesty and valued our principle of need to care</h6>
                                <h6><span class="text-primary">Diversity and Inclusion:</span> our strength lies in diversity and inclusive environment to improve the health outcomes of the populations we serve.</h6>
                            <h6><span class="text-primary">Commitment:</span> we are awake every hour our patients need us. No patients leave behind. We proud ourselves in universal health as a human right. We are 360 degrees 24 hours operating health facility.</h6>
                            <h6><span class="text-primary">Mutual Respect:</span> we listen to the needs and concerns of every individual we serve. We are nonjudgmental in any kind. </h6>
