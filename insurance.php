                                <h5>EHS DESCRIPTION</h5>
                                <h6>
                                    <p class="justify-content-around">

                                        EHS delivers compulsory universal coverage—which means every resident has access to healthcare regardless of their ability to pay. EHS compulsory health system provides patients with Access Card (AC).
                                        Each individual patient has an AC with a unique personal identification number or electronic medical record number assigned. We use this number in our secure electronic database. We have partnered with Vanuston Intelligence Private Ltd in India for our electronic health record system (EHR).
                                    </p><p>EHS has a single payer through the Universal health insurance system (UHI). Patients are not required to pay for cost of medical care unless they are being transferred to non-EHS facility. EHS covers up to 100 percent of the patient medical cost. Patients are encouraged to use their AC in any EHS facility in Liberia.</p>
                                    <p>
                                        The AC is universal, portable, and accessible. As we plan to expand our services throughout Liberia, we currently operate in Lofa County. EHS provide essential health services in primary care, Obstetrics and Gynecology, laboratory testing, consultation, prescription drugs, and length of stay (admission).</p>
                                    <p>Patients who wish to use EHS universal coverage to transfer to another facility might do so with the choice of top-up which might be less than 50 percent transfer fees to that facility. Every facility has policy and procedure that must be followed. EHS has no jurisdiction over these policies. However, in the case of readmission to EHS facility, patients will have the same right under the universal health insurance policy.</p>
                                    <p>                                </p>
                                    <p>Every patient regardless of their socioeconomic (SES) is covered by UHI support and serve according to EHS mission. Regardless of high SES or low SES, every patient is required to go through the universal health insurance process that is equal and fair.



                                    </p>
                                    <p>Patients who fail to go through the UHI process, which is to meet with UHI counselor and complete documentation could risk losing coverage. No exception. For transparency, we will periodically post on our website the types of disease and conditions that are commonly treated in our facility.</p>


                                </h6>


                                    <h4>Patients with Special Needs or Disability</h4>
<h5>                                    The UHI counselor has the responsibility to access patients for disability. Patients with disability and special needs do not pay cost-sharing for transfer. UHI has the jurisdiction to cover 100 percent cost for patients with disability and special need. UHI shall consider patients disable or in special need with a general term if they have some type of impairments in functioning not limited to amputation, HIV/AIDS, chronic mental health disorder, visual impairment, deafness, and chronic migraine among others. The list is not exhaustive. Special need patients should meet with the counselors to discuss their benefit and to know how EHS can provide service for them.
</h5>

<h4>
    Seniors 65 years and older

                            </h4>
                                <h5>                                    Patients 65 years and older are automatically enrolled in the UHI program without cost-sharing transfer fees to non-EHS facility. If EHS must do an external referral for any patients, the UHI counselor will work with the patient to smooth this transition. Patients that are transferred to an external facility are no longer ensured by UHI. These facilities have their own policy that EHS has no jurisdiction over. EHS team should provide transportation for patients to the external facility at no cost.
                                </h5>


                                <h4>
                                    Student Health
                        </h4>
                                <h5>                                    EHS connects with students in their respective schools. Essential Health Services are free for all students. Students are required to fill in EHS form on their campus located in their principal's office. There are no fees to obtain the form. It is recommended that students fill in the form and have their principal sign it and take it to the clinic.
                                </h5>
                                <h4>
                                    Referral System

                    </h4>
                                <h5>                                    EHS will partner with local hospitals and international nongovernmental organizations to coordinate referral system. Patients who wish to transfer their care to an external facility will be coordinated in a timely manner. Patients with complicated cases, if EHS, in any event, cannot handle these cases will discuss with the patient to determine the appropriate referral site of referral.
                                </h5>
