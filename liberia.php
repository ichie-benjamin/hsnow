                                <h5>Our target country</h5>
                                <h6>
                                    <p class="justify-content-around">
                                        Changing the Outskirt of the Liberian Health System

                                    </p><p> Liberia has an unstructured health care system. Healthcare services are provided through pay out-of-pocket system. Which means care is delivered on the basis by how much patient can afford. This model creates health disparity.</p>
                                    <p>The vulnerable are most likely to be under served. The poor people suffer from this type of system. They are not protected from financial hardship that is caused by medical expenses. Patients who cannot afford private clinic end up in crowded and sometime unsanitary government operated facilities.</p>
                                    <p>      EHS solution is to change this; patients should be treated equally, fairly with dignity and respect regardless of their ability to pay.                          </p>

<p>            It is also known for limited resources to keep up with the growing demand of healthcare consumption. Despite the government spending 10% of gross domestic product (GDP) on healthcare, so many Liberians go without access to essential health coverage. In 2010 an estimated 0.02 physicians served nearly 1000 populations. There is a limited number of hospital and clinic in the country. An estimated 0.8 hospital beds could serve 1000 inhabitants. The health sector is understaffed. There is a dire need for nurses, midwives, and doctors. Eli Health System approach is to build health system that foster the training of nurses and doctors in modern healthcare. EHS will provide support to nurses and doctors to have the necessary tool needed to care for our patients. EHS is one in few if any to introduce electronic health record system in Liberia. We aim to reduce medical waste and increase transparency in the healthcare industry.


</p>
                                    <h4>Why Liberia?</h4>
<p>                                    Liberia is one of the poorest countries in the world. Healthcare infrastructure was destroyed during the country 14 years of civil war. Healthcare infrastructure remains a significant challenge. Health system in Liberia mostly depends on foreign aid and international nongovernmental organizations. Healthcare consumption is out-of-pocket fees for service. Out-of-pocket healthcare expenditure can expose patients to financial hardship.
                                    </p>


